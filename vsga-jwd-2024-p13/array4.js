/**
 * Array pada JavaScript
 */ 

// Mendeklarasikan dan menginisialisasi array dengan elemen
let colors = ['red', 'green', 'blue'];
console.log('Define: ');
console.log(colors);

// Menambahkan elemen di akhir array
colors.push('yellow');
console.log('Push: ');
console.log(colors);

// Menambahkan elemen di awal array
colors.unshift('orange');
console.log('Unshift: ');
console.log(colors);

// Menghapus elemen di akhir array
colors.pop();
console.log('Pop: ');
console.log(colors);

// Menghapus elemen di awal array
colors.shift();
console.log('Shift: ');
console.log(colors);
